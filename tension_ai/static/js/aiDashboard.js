// AI Dashboard state controller
let currentLayoutId = 11; // Default to Spray L11
let currentSizeId = 8; // Default size 12x8
let currentGradeValue = 5; // Default slider value
let currentTempValue = 1.5; // Default temperature
let currentAngleValue = 40; // Default angle
let currentNoMatchValue = true; // Default no match

// Climb data state
let currentClimb = {
  grade: "V5",
  frames: "", // Placements for current layout
  raw_frames: "" // Raw placements generated by model (L11 Spray)
};
let isMirrored = false;

// Toggles and inputs
const btnSpray = document.getElementById("btn-layout-spray");
const btnMirror = document.getElementById("btn-layout-mirror");
const sizeSelect = document.getElementById("select-size");
const gradeSlider = document.getElementById("grade-slider");
const gradeDisplay = document.getElementById("grade-display");
const tempSlider = document.getElementById("temp-slider");
const tempDisplay = document.getElementById("temp-display");
const angleSlider = document.getElementById("angle-slider");
const angleDisplay = document.getElementById("angle-display");
const switchNoMatch = document.getElementById("switch-nomatch");
const btnGenerate = document.getElementById("btn-generate");
const btnMirrorToggle = document.getElementById("button-mirror");

const climbGradeEl = document.getElementById("climb-grade");
const climbAngleEl = document.getElementById("climb-angle");
const climbNoMatchEl = document.getElementById("climb-nomatch");
const svgElement = document.getElementById("svg-climb");
const aiThinker = document.getElementById("ai-thinker");
const thinkingStep = document.getElementById("thinking-step");

// Mappings for sliders
const indexToGradeMap = [
  "V0", "V1", "V2", "V3", "V4", "V5", "V6", "V7", "V8", "V9", "V10", "V11", "V12", "V13", "V14", "V15"
];


// Mappings for instant client-side hold translation between layouts
const placementToCoordMap11 = {};
const coordToPlacementMap10 = {};

// Start initialization
document.addEventListener("DOMContentLoaded", () => {
  initializeDashboard();
});

function initializeDashboard() {
  // Populate the client-side placement coordinate mappings (using size 8 as full bounds baseline)
  buildClientSideMappings();

  // Sync NoMatch switch state on load (force it ON by default to override browser cache)
  if (switchNoMatch) {
    switchNoMatch.checked = true;
    currentNoMatchValue = true;
  }

  // Render board for the default layout
  renderBoard(currentLayoutId);

  // Mirror toggle button starts hidden because Spray (11) is the default board layout
  btnMirrorToggle.classList.add("d-none");

  // Setup layout toggles
  btnSpray.addEventListener("click", () => switchLayout(11));
  btnMirror.addEventListener("click", () => switchLayout(10));

  // Size selector dropdown
  sizeSelect.addEventListener("change", (e) => {
    currentSizeId = parseInt(e.target.value);
    renderBoard(currentLayoutId);

    // Clear active climb immediately
    currentClimb.raw_frames = "";
    currentClimb.frames = "";
    resetBoardHolds();
  });

  // Grade slider
  gradeSlider.addEventListener("input", (e) => {
    const val = parseInt(e.target.value);
    currentGradeValue = val;
    gradeDisplay.textContent = indexToGradeMap[val];
  });

  // Temperature slider
  tempSlider.addEventListener("input", (e) => {
    const val = parseFloat(e.target.value);
    currentTempValue = val;
    tempDisplay.textContent = val.toFixed(1);
  });

  // Angle slider
  angleSlider.addEventListener("input", (e) => {
    const val = parseInt(e.target.value);
    currentAngleValue = val;
    angleDisplay.textContent = `${val}°`;
  });

  // No Match switch
  switchNoMatch.addEventListener("change", (e) => {
    currentNoMatchValue = e.target.checked;
  });

  // Generate button
  btnGenerate.addEventListener("click", generateClimb);

  // Mirror toggle button
  btnMirrorToggle.addEventListener("click", toggleMirror);
}

function buildClientSideMappings() {
  // Map Layout 11 placements to physical x,y coordinates using size 10 as full boundaries baseline
  const config11 = configs["11"]["10"];
  if (config11) {
    for (const holds of Object.values(config11.images_to_holds)) {
      for (const [holdId, mirroredHoldId, x, y] of holds) {
        placementToCoordMap11[holdId.toString()] = `${x},${y}`;
      }
    }
  }

  // Map physical x,y coordinates to Layout 10 placements using size 10 as full boundaries baseline
  const config10 = configs["10"]["10"];
  if (config10) {
    for (const holds of Object.values(config10.images_to_holds)) {
      for (const [holdId, mirroredHoldId, x, y] of holds) {
        coordToPlacementMap10[`${x},${y}`] = holdId;
      }
    }
  }
}

function translateFrames11To10(framesString11) {
  let mapped = "";
  const holds = framesString11.split("p");
  for (const hold of holds) {
    if (!hold) continue;
    const [placementId11, colorId] = hold.split("r");

    // Find the physical coordinate of this hold
    const coordKey = placementToCoordMap11[placementId11];
    if (coordKey) {
      // Find the corresponding hold ID on Layout 10
      const placementId10 = coordToPlacementMap10[coordKey];
      if (placementId10) {
        mapped = mapped + "p" + placementId10 + "r" + colorId;
      }
    }
  }
  return mapped;
}

function renderBoard(layoutId) {
  // Clear previous SVG contents
  svgElement.innerHTML = "";
  isMirrored = false;
  btnMirrorToggle.classList.remove("active");

  const config = configs[layoutId.toString()][currentSizeId.toString()];
  if (!config) return;

  // Use the drawBoard function from common.js
  drawBoard(
    "svg-climb",
    config.images_to_holds,
    config.edge_left,
    config.edge_right,
    config.edge_bottom,
    config.edge_top,
    null // No circle click handlers needed for viewing
  );
}

function switchLayout(layoutId) {
  if (currentLayoutId === layoutId) return;

  currentLayoutId = layoutId;

  // Toggle active UI classes
  if (layoutId === 11) {
    btnSpray.classList.add("active");
    btnMirror.classList.remove("active");
    btnMirrorToggle.classList.add("d-none"); // Hide mirror toggle on Spray layout
    isMirrored = false;
    btnMirrorToggle.classList.remove("active");
  } else {
    btnSpray.classList.remove("active");
    btnMirror.classList.add("active");
    btnMirrorToggle.classList.remove("d-none"); // Show mirror toggle on Mirror layout
  }

  // Redraw the SVG Board
  renderBoard(layoutId);

  // If there's an active climb, we want to translate and draw it
  if (currentClimb.raw_frames) {
    remapAndDrawActiveClimb();
  }
}

function remapAndDrawActiveClimb() {
  // Clear currently highlighted holds
  resetBoardHolds();

  let mappedFrames = "";

  if (currentLayoutId === 11) {
    mappedFrames = currentClimb.raw_frames;
  } else {
    // Translate L11 raw frames to L10 mapping coordinates instantly on client side
    mappedFrames = translateFrames11To10(currentClimb.raw_frames);
  }

  currentClimb.frames = mappedFrames;
  highlightClimb(mappedFrames);
}

function resetBoardHolds() {
  const circles = svgElement.querySelectorAll("circle");
  circles.forEach((circle) => {
    circle.setAttribute("stroke-opacity", 0.0);
    circle.setAttribute("stroke", "black");
  });
}

function getMirroredFrames(framesString) {
  let mirrored = "";
  const holds = framesString.split("p");
  for (const hold of holds) {
    if (!hold) continue;
    const [placementId, colorId] = hold.split("r");
    const circle = document.getElementById(`hold-${placementId}`);
    if (circle && circle.hasAttribute("data-mirror-id")) {
      const mirroredPlacementId = circle.getAttribute("data-mirror-id");
      mirrored = mirrored + "p" + mirroredPlacementId + "r" + colorId;
    }
  }
  return mirrored;
}

function highlightClimb(framesString) {
  resetBoardHolds();
  if (!framesString) return;

  const config = configs[currentLayoutId.toString()][currentSizeId.toString()];
  const colorMap = config.colors.reduce((acc, colorRow) => {
    acc[colorRow[0]] = colorRow[1];
    return acc;
  }, {});

  const holds = framesString.split("p");
  for (const hold of holds) {
    if (!hold) continue;
    const [placementId, colorId] = hold.split("r");
    const circle = document.getElementById(`hold-${placementId}`);
    if (circle) {
      circle.setAttribute("stroke", colorMap[colorId]);
      circle.setAttribute("stroke-opacity", 1.0);
    }
  }
}

function toggleMirror() {
  if (!currentClimb.frames) return;

  isMirrored = !isMirrored;

  if (isMirrored) {
    btnMirrorToggle.classList.add("active");
    // Swap holds to mirror positions
    const mirrored = getMirroredFrames(currentClimb.frames);
    highlightClimb(mirrored);
  } else {
    btnMirrorToggle.classList.remove("active");
    // Restore original holds
    highlightClimb(currentClimb.frames);
  }
}

function generateClimb() {
  // Show spinner
  aiThinker.classList.remove("d-none");
  thinkingStep.textContent = "Connecting to Neural Engine...";

  // Rotate thinking status text for AI flavor
  const steps = [
    "Connecting to Neural Engine...",
    "Seeding model difficulty context...",
    "Predicting structural movement path...",
    "Applying physics hold guardrails...",
    "Verifying movement flow..."
  ];
  let stepIdx = 0;
  const statusInterval = setInterval(() => {
    stepIdx = (stepIdx + 1) % steps.length;
    thinkingStep.textContent = steps[stepIdx];
  }, 700);

  const targetGrade = indexToGradeMap[currentGradeValue];

  fetch("/api/v1/generate", {
    method: "POST",
    headers: {
      "Content-Type": "application/json"
    },
    body: JSON.stringify({
      layout_id: currentLayoutId,
      size_id: currentSizeId,
      grade: targetGrade,
      temperature: currentTempValue,
      angle: currentAngleValue,
      is_nomatch: currentNoMatchValue
    })
  })
    .then(async (res) => {
      if (!res.ok) {
        let errMsg = "Failed to reach generative engine. Make sure python server is running correctly.";
        try {
          const errData = await res.json();
          if (errData && errData.description) {
            errMsg = errData.description;
          }
        } catch (e) { }
        throw new Error(errMsg);
      }
      return res.json();
    })
    .then((data) => {
      clearInterval(statusInterval);
      aiThinker.classList.add("d-none");

      if (data.error) {
        alert("AI Model Generation Error: " + data.description);
        return;
      }

      // Update state
      currentClimb = {
        grade: targetGrade,
        frames: data.frames,
        raw_frames: data.frames
      };

      isMirrored = false;
      btnMirrorToggle.classList.remove("active");

      // Update UI if elements exist
      if (climbGradeEl) climbGradeEl.textContent = targetGrade;
      if (climbAngleEl) climbAngleEl.textContent = `${currentAngleValue}°`;
      if (climbNoMatchEl) climbNoMatchEl.textContent = currentNoMatchValue ? "Yes" : "No";

      // Draw the climb holds on the board
      highlightClimb(data.frames);
    })
    .catch((err) => {
      clearInterval(statusInterval);
      aiThinker.classList.add("d-none");
      console.error(err);
      alert(err.message);
    });
}
